package aero.s7.jl.autotest.api.Dictionary;

public class Type {
    private int typeId;
    private String name;
    private boolean additional;

    public Type() {
    }

    public int getTypeId() {
        return typeId;
    }
    public String getName() {
        return name;
    }
    public boolean isAdditional() {
        return additional;
    }
}
